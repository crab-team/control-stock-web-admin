class Product {
  final String id;
  final String code;
  final String name;
  final String type;
  final double price;
  final int stock;
  final String imageUrl;

  Product({
    required this.id,
    required this.code,
    required this.name,
    required this.type,
    required this.price,
    required this.stock,
    required this.imageUrl,
  });
}
